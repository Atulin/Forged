using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace Forged.Generator;

[Generator]
public class ForgedGenerator : IIncrementalGenerator
{
	public void Initialize(IncrementalGeneratorInitializationContext context)
	{
		var typesToGenerate = context.SyntaxProvider
			.ForAttributeWithMetadataName(
				"Forged.Core.FakeAttribute",
				predicate: static (s, _) => s is ClassDeclarationSyntax or RecordDeclarationSyntax,
				transform: static (ctx, _) => GetSemanticTargetForGeneration(ctx.SemanticModel, ctx.TargetNode)
			)
			.Where(static m => m is not null);

		context.RegisterSourceOutput(typesToGenerate, static (spc, source) => Execute(spc, source));
	}

	private static TypeToGenerate? GetSemanticTargetForGeneration(SemanticModel semanticModel, SyntaxNode node)
	{
		if (semanticModel.GetDeclaredSymbol(node) is not INamedTypeSymbol symbol)
		{
			return null;
		}

		var properties = symbol.GetMembers()
			.OfType<IPropertySymbol>()
			.Where(p => p.SetMethod is not null && p.DeclaredAccessibility == Accessibility.Public)
			.Select(p => new PropertyToGenerate(
				name: p.Name,
				type: p.Type.ToDisplayString(),
				isRequired: p.IsRequired
			))
			.ToList();

		return new TypeToGenerate(
			symbol.ContainingNamespace.IsGlobalNamespace ? string.Empty : symbol.ContainingNamespace.ToDisplayString(),
			name: symbol.Name,
			properties: properties.ToEquatableReadOnlyList()
		);
	}

	private static void Execute(SourceProductionContext context, TypeToGenerate? typeToGenerate)
	{
		if (typeToGenerate is null) return;

		var sb = new StringBuilder();
		sb.AppendLine("// <auto-generated/>");
		sb.AppendLine("#nullable enable");
		sb.AppendLine("using System;");
		sb.AppendLine("using System.Collections.Generic;");
		sb.AppendLine("using Forged.Core;");
		sb.AppendLine("using Forged.Core.Generators;");

		if (!string.IsNullOrEmpty(typeToGenerate.Namespace))
		{
			sb.AppendLine();
			sb.AppendLine($"namespace {typeToGenerate.Namespace};");
		}

		sb.AppendLine();
		sb.AppendLine($"public partial class {typeToGenerate.Name}Faker : Faker<{typeToGenerate.Name}>");
		sb.AppendLine("{");

		foreach (var prop in typeToGenerate.Properties)
		{
			if (prop.IsRequired)
			{
				sb.AppendLine($"    public required Func<Forge, IGenerator<{prop.Type}>> {prop.Name} {{ get; init; }}");
			}
			else
			{
				sb.AppendLine($"    public Func<Forge, IGenerator<{prop.Type}>>? {prop.Name} {{ get; init; }}");
			}
		}

		sb.AppendLine();

		foreach (var prop in typeToGenerate.Properties)
		{
			sb.AppendLine($"    private IGenerator<{prop.Type}>? _{Decapitalize(prop.Name)}Generator;");
		}

		sb.AppendLine();
		sb.AppendLine($"    public {typeToGenerate.Name}Faker(Random? random = null) : base(random) {{ }}");

		sb.AppendLine();
		sb.AppendLine($"    public override {typeToGenerate.Name} Get()");
		sb.AppendLine("    {");

		foreach (var prop in typeToGenerate.Properties)
		{
			var fieldName = $"_{Decapitalize(prop.Name)}Generator";
			if (prop.IsRequired)
			{
				sb.AppendLine($"        {fieldName} ??= {prop.Name}(this.Forge);");
			}
			else
			{
				sb.AppendLine($"        {fieldName} ??= {prop.Name}?.Invoke(this.Forge);");
			}
		}

		sb.AppendLine();
		sb.AppendLine($"        return new {typeToGenerate.Name}");
		sb.AppendLine("        {");

		foreach (var prop in typeToGenerate.Properties)
		{
			var fieldName = $"_{Decapitalize(prop.Name)}Generator";
			if (prop.IsRequired)
			{
				sb.AppendLine($"            {prop.Name} = {fieldName}.Generate(),");
			}
			else
			{
				sb.AppendLine($"            {prop.Name} = {fieldName} is not null ? {fieldName}.Generate() : default!,");
			}
		}

		sb.AppendLine("        };");
		sb.AppendLine("    }");
		sb.AppendLine("}");

		context.AddSource($"{typeToGenerate.Name}Faker.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
	}

	private static string Decapitalize(string input)
	{
		var span = input.AsSpan();
		Span<char> result = stackalloc char[span.Length];
		result[0] = char.ToLowerInvariant(span[0]);
		span.Slice(1).CopyTo(result.Slice(1));
		return result.ToString();
	}
}

public class TypeToGenerate(string ns, string name, EquatableReadOnlyList<PropertyToGenerate> properties)
{
	public string Namespace { get; } = ns;
	public string Name { get; } = name;
	public EquatableReadOnlyList<PropertyToGenerate> Properties { get; } = properties;
}

public class PropertyToGenerate(string name, string type, bool isRequired)
{
	public string Name { get; } = name;
	public string Type { get; } = type;
	public bool IsRequired { get; } = isRequired;
}
